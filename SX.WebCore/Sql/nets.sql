DECLARE @id INT
DECLARE c CURSOR LOCAL FORWARD_ONLY FAST_FORWARD FOR
SELECT dn.Id FROM D_NET AS dn
OPEN c
FETCH NEXT FROM c INTO @id
WHILE @@FETCH_STATUS=0
BEGIN
	IF NOT EXISTS (SELECT*FROM D_LIKE_BUTTON AS dlb WHERE dlb.NetId=@id)
	INSERT INTO D_LIKE_BUTTON
	(
		NetId,
		Show,
		ShowCounter,
		DateUpdate,
		DateCreate
	)
	VALUES
	(
		@id,
		0,
		0,
		GETDATE(),
		GETDATE()
	)
	
	FETCH NEXT FROM c INTO @id
END
CLOSE c
DEALLOCATE c